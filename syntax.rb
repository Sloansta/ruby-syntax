# Comment in Ruby

print "enter a value"

first_num = gets.to_i

print "enter another value"

second_num = gets.to_i

puts first_num.to_s + " + " + second_num.to_s + " = " + (first_num + second_num).to_s

num_one = 1.000

num99 = 0.999

puts num_one.to_s + " - " + num99.to_s + " = " + (num_one - num99).to_s